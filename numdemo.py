import numpy as np
myarray=np.array([100,95,75,88,40,70])

maximum=np.max(myarray)
print(maximum)

minimum=np.min(myarray)
print(minimum)

total=np.sum(myarray)
print(total)

totalproduct=np.prod(myarray)
print(totalproduct)

meanvalue=np.mean(myarray)
print(meanvalue)

stddeviation=np.std(myarray)
print(stddeviation)